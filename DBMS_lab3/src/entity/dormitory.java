package entity;

public class dormitory {

	private String room_num;
	private int bed_sum;
	public String getRoom_num() {
		return room_num;
	}
	public void setRoom_num(String room_num) {
		this.room_num = room_num;
	}
	public int getBed_sum() {
		return bed_sum;
	}
	public void setBed_sum(int bed_sum) {
		this.bed_sum = bed_sum;
	}
	
}
